#include "push_swap.h"

// =============================================================================
// 									main
// =============================================================================

int main(int ac, char **av)
{
	t_stack *a;
	t_stack *b;

	a = NULL;
	b = NULL;

	if (init_stack(&a, ac, av) == ERROR)
		return (ERROR);

	if (ac > 2)
	{
		if (!is_sorted(a))
			sort_stack(&a, &b);
		free_stack(a);
		free_stack(b);
	}
	return (SUCCESS);
}
